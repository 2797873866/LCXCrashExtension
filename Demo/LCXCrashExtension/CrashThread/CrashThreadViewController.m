@implementation CrashThreadViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    [self threadSafe];
}

#pragma mark thread safe

- (void)threadSafe {
    for (int i = 0; i < 10000; i++){
        dispatch_async(dispatch_get_global_queue(DISPATCH_QUEUE_PRIORITY_DEFAULT, 0), ^{
            self.model = [TestModel new];
        });
        dispatch_async(dispatch_get_global_queue(DISPATCH_QUEUE_PRIORITY_DEFAULT, 0), ^{
            self.model = [TestModel new];
        });
        dispatch_async(dispatch_get_global_queue(DISPATCH_QUEUE_PRIORITY_DEFAULT, 0), ^{
            self.model = [TestModel new];
        });
    }
}

